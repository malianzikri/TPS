public class EntityLog {
   private String user;
   private String nama_class;
   private String refNumber;
   private String jnsDok;
   private String Dok;

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getNama_class() {
        return nama_class;
    }

    public void setNama_class(String nama_class) {
        this.nama_class = nama_class;
    }

    public String getRefNumber() {
        return refNumber;
    }

    public void setRefNumber(String refNumber) {
        this.refNumber = refNumber;
    }

    public String getJnsDok() {
        return jnsDok;
    }

    public void setJnsDok(String jnsDok) {
        this.jnsDok = jnsDok;
    }

    public String getDok() {
        return Dok;
    }

    public void setDok(String Dok) {
        this.Dok = Dok;
    }
   
}
